def translate_html(source_path, dest_path, dictionary):
    with open(source_path, 'r', encoding='utf-8') as f:
        content = f.read()
    
    # Sort dictionary by length descending to prevent partial replacements
    sorted_dict = sorted(dictionary.items(), key=lambda x: len(x[0]), reverse=True)
    
    for ru, trans in sorted_dict:
        content = content.replace(ru, trans)
        
    with open(dest_path, 'w', encoding='utf-8') as f:
        f.write(content)


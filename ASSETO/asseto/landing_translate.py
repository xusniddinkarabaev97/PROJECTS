# Update index.html, index_en.html, index_uz.html to have a language switcher
def add_switcher(filepath, current_lang):
    with open(filepath, 'r', encoding='utf-8') as f:
        html = f.read()
    
    # Simple CSS to style the switcher gracefully
    switcher = f"""
    <div class="lang-switcher" style="display:flex; gap:12px; margin-left:24px; font-size:13px; font-weight:800;">
      <a href="index.html" style="color: {'var(--accent)' if current_lang == 'ru' else 'var(--text2)'}; text-decoration:none; transition:0.2s;">RU</a>
      <a href="index_en.html" style="color: {'var(--accent)' if current_lang == 'en' else 'var(--text2)'}; text-decoration:none; transition:0.2s;">EN</a>
      <a href="index_uz.html" style="color: {'var(--accent)' if current_lang == 'uz' else 'var(--text2)'}; text-decoration:none; transition:0.2s;">UZ</a>
    </div>
    """
    
    # Avoid double insertion
    if 'class="lang-switcher"' not in html:
        html = html.replace('<div class="nav-cta">', switcher + '\n    <div class="nav-cta">')
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(html)

add_switcher('landing/index.html', 'ru')
add_switcher('landing/index_en.html', 'en')
add_switcher('landing/index_uz.html', 'uz')

